export class Course {
  constructor (title, description, photo, level, participants, duration, topics, skills, instructors) {
    this.title = title
    this.description = description
    this.photo = photo
    this.level = level
    this.participants = participants
    this.duration = duration
    this.topics = topics
    this.skills = skills
    this.instructors = instructors
  }
}
